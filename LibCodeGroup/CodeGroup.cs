        public static string[] GenerateLookup()
        {
            lookup = Generate.CodeGroup2Lookup();
            return lookup;
        }

        public static string ConvertToCode(byte[] data)
        {
            StringBuilder sb = new StringBuilder();

            for (int i = 0; i < data.Length; i++)
                sb.Append(lookup[(int)data[i]]);

            return sb.ToString();
        }

        public static byte[] ConvertFromCode(string data)
        {
            byte[] decoded = new byte[data.Length / 2];
            int index = 0;

            for (int i = 0; i < decoded.Length; i++)
                decoded[i] = (byte)FindIndex(data[index++].ToString() + data[index++].ToString());
                
            return decoded;
        }

        private static int FindIndex(string data)
        {
            for(int i = 0; i < lookup.Length; i++)
                if (lookup[i] == data)
                    return i;

            return -1;
        }
